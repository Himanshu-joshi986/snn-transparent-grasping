# ──────────────────────────────────────────────────────────────────────
# Augmentation pipelines
# ──────────────────────────────────────────────────────────────────────

def get_train_augmentations(image_size: Tuple[int, int]) -> A.Compose:
    H, W = image_size
    return A.Compose([
        A.Resize(H, W),
        A.HorizontalFlip(p=0.5),
        A.ShiftScaleRotate(shift_limit=0.05, scale_limit=0.1, rotate_limit=15, p=0.5),
        A.OneOf([
            A.GaussNoise(var_limit=(0.001, 0.01), p=1.0),
            A.GaussianBlur(blur_limit=3, p=1.0),
        ], p=0.3),
        A.RandomBrightnessContrast(brightness_limit=0.2, contrast_limit=0.2, p=0.4),
        A.Normalize(mean=0.0, std=1.0),  # events already in [-1, 1]
    ], additional_targets={"mask": "mask"})


def get_val_augmentations(image_size: Tuple[int, int]) -> A.Compose:
    H, W = image_size
    return A.Compose([
        A.Resize(H, W),
        A.Normalize(mean=0.0, std=1.0),
    ], additional_targets={"mask": "mask"})


# ──────────────────────────────────────────────────────────────────────
# Event Voxel Grid utilities
# ──────────────────────────────────────────────────────────────────────

def events_to_voxel_grid(
    events: np.ndarray,
    num_bins: int,
    height: int,
    width: int,
    normalize: bool = True,
) -> np.ndarray:
    """
    Convert raw events to a voxel grid (time-surface histogram).

    Args:
        events: (N, 4) array with columns [t, x, y, p]
                  t: timestamp (normalised to [0, 1])
                  x, y: pixel coordinates
                  p: polarity (+1 / -1)
        num_bins: number of time bins (T)
        height, width: spatial dimensions
        normalize: normalise each bin by its max absolute value

    Returns:
        voxel: (2, T, H, W) float32 array (channel 0=pos, channel 1=neg)
    """
    assert events.shape[1] == 4, "Events must be (N, 4): [t, x, y, p]"
    voxel = np.zeros((2, num_bins, height, width), dtype=np.float32)

    if len(events) == 0:
        return voxel

    t = events[:, 0].astype(np.float64)
    x = events[:, 1].astype(np.int32)
    y = events[:, 2].astype(np.int32)
    p = events[:, 3].astype(np.float32)

    # Clip coordinates
    valid = (x >= 0) & (x < width) & (y >= 0) & (y < height)
    t, x, y, p = t[valid], x[valid], y[valid], p[valid]

    # Normalise timestamps to [0, num_bins)
    t_norm = (t - t.min()) / (t.max() - t.min() + 1e-9) * (num_bins - 1)
    bin_idx = np.floor(t_norm).astype(np.int32).clip(0, num_bins - 1)

    # Bilinear temporal interpolation
    t_frac = t_norm - bin_idx
    bin_next = (bin_idx + 1).clip(0, num_bins - 1)

    pos_mask = p > 0
    neg_mask = ~pos_mask

    for polarity, mask in [(0, pos_mask), (1, neg_mask)]:
        if not mask.any():
            continue
        np.add.at(voxel[polarity, bin_idx[mask], y[mask], x[mask]],
                  np.arange(mask.sum()), (1 - t_frac[mask]))
        np.add.at(voxel[polarity, bin_next[mask], y[mask], x[mask]],
                  np.arange(mask.sum()), t_frac[mask])

    if normalize:
        for b in range(num_bins):
            for c in range(2):
                m = np.abs(voxel[c, b]).max()
                if m > 0:
                    voxel[c, b] /= m

    return voxel


def simulate_events_from_frames(
    frame1: np.ndarray,
    frame2: np.ndarray,
    threshold: float = 0.15,
    num_bins: int = 10,
) -> np.ndarray:
    """
    Simple frame-differencing event simulation (fallback when v2e is unavailable).

    Args:
        frame1, frame2: grayscale uint8 frames
        threshold: log-contrast threshold
        num_bins: temporal bins

    Returns:
        events: (N, 4) array [t, x, y, p]
    """
    f1 = frame1.astype(np.float32) / 255.0 + 1e-6
    f2 = frame2.astype(np.float32) / 255.0 + 1e-6
    log_diff = np.log(f2) - np.log(f1)

    H, W = log_diff.shape[:2]
    events = []

    # Generate events where log-contrast exceeds threshold
    pos_ys, pos_xs = np.where(log_diff > threshold)
    neg_ys, neg_xs = np.where(log_diff < -threshold)

    for i, (ys, xs, pol) in enumerate([(pos_ys, pos_xs, 1), (neg_ys, neg_xs, -1)]):
        for t_bin in range(num_bins):
            # Jitter timestamps within the bin
            ts = np.random.uniform(t_bin / num_bins, (t_bin + 1) / num_bins, len(ys))
            pols = np.full(len(ys), pol)
            if len(ys) > 0:
                events.append(np.stack([ts, xs, ys, pols], axis=1))

    return np.concatenate(events, axis=0) if events else np.zeros((0, 4), dtype=np.float32)


# ──────────────────────────────────────────────────────────────────────
# Dataset
# ──────────────────────────────────────────────────────────────────────

class EventDataset(Dataset):
    """
    ClearGrasp event dataset.

    Loads pre-generated event voxel grids (.npy) and segmentation masks.
    Falls back to frame-differencing simulation if event files are missing.

    Args:
        rgb_dir:    Path to RGB images.
        mask_dir:   Path to ground truth segmentation masks.
        event_dir:  Path to pre-generated event voxel grids (.npy).
        image_size: (H, W) target resolution.
        time_steps: Number of temporal bins T.
        split:      "train" | "val" | "test"
        transform:  Albumentations augmentation pipeline.
    """

    def __init__(
        self,
        rgb_dir: str,
        mask_dir: str,
        event_dir: str,
        image_size: Tuple[int, int] = (256, 256),
        time_steps: int = 10,
        split: str = "train",
        transform: Optional[Callable] = None,
    ):
        self.rgb_dir    = Path(rgb_dir)
        self.mask_dir   = Path(mask_dir)
        self.event_dir  = Path(event_dir)
        self.image_size = image_size
        self.time_steps = time_steps
        self.split      = split
        self.transform  = transform

        # Collect samples
        self.samples = self._collect_samples()
        if len(self.samples) == 0:
            logger.warning(
                f"No samples found in {rgb_dir}. "
                "Run utils/generate_events.py first or check paths."
            )
        else:
            logger.info(f"[{split.upper()}] Found {len(self.samples)} samples.")

    # ------------------------------------------------------------------
    def _collect_samples(self) -> List[dict]:
        samples = []
        extensions = ["*.png", "*.jpg", "*.jpeg"]
        rgb_files = []
        for ext in extensions:
            rgb_files.extend(sorted(self.rgb_dir.glob(ext)))

        for rgb_path in rgb_files:
            stem = rgb_path.stem
            # Search for mask
            mask_path = None
            for ext in [".png", ".jpg", "_mask.png", "_segmentation.png"]:
                candidate = self.mask_dir / f"{stem}{ext}"
                if candidate.exists():
                    mask_path = candidate
                    break

            if mask_path is None:
                continue

            # Search for pre-generated event file
            event_path = self.event_dir / f"{stem}.npy"
            event_path_fallback = self.event_dir / f"{stem}_events.npy"

            samples.append({
                "rgb":   rgb_path,
                "mask":  mask_path,
                "event": event_path if event_path.exists()
                         else (event_path_fallback if event_path_fallback.exists()
                               else None),
            })
        return samples

    # ------------------------------------------------------------------
    def _load_event_voxel(self, sample: dict) -> np.ndarray:
        """
        Load or simulate event voxel grid. Shape: (2, T, H, W).
        """
        H, W = self.image_size

        if sample["event"] is not None:
            try:
                voxel = np.load(sample["event"])
                # Resize if needed
                if voxel.shape[-2:] != (H, W):
                    C, T = voxel.shape[:2]
                    resized = np.zeros((C, T, H, W), dtype=np.float32)
                    for c in range(C):
                        for t in range(T):
                            resized[c, t] = cv2.resize(
                                voxel[c, t], (W, H), interpolation=cv2.INTER_LINEAR
                            )
                    voxel = resized
                return voxel.astype(np.float32)
            except Exception as e:
                logger.debug(f"Failed to load {sample['event']}: {e}")

        # Fallback: simulate from RGB frame pair
        rgb = cv2.imread(str(sample["rgb"]), cv2.IMREAD_GRAYSCALE)
        rgb = cv2.resize(rgb, (W, H))
        # Create synthetic 'next frame' with small random motion
        M = np.float32([[1, 0, random.uniform(-2, 2)],
                        [0, 1, random.uniform(-2, 2)]])
        rgb2 = cv2.warpAffine(rgb, M, (W, H))
        events = simulate_events_from_frames(rgb, rgb2, num_bins=self.time_steps)
        return events_to_voxel_grid(events, self.time_steps, H, W)

    # ------------------------------------------------------------------
    def __len__(self) -> int:
        return len(self.samples)

    # ------------------------------------------------------------------
    def __getitem__(self, idx: int) -> dict:
        sample = self.samples[idx]
        H, W = self.image_size

        # Load mask
        mask = cv2.imread(str(sample["mask"]), cv2.IMREAD_GRAYSCALE)
        mask = cv2.resize(mask, (W, H), interpolation=cv2.INTER_NEAREST)
        mask = (mask > 127).astype(np.float32)

        # Load event voxel (C=2, T, H, W)
        voxel = self._load_event_voxel(sample)                 # (2, T, H, W)

        # Apply spatial augmentations to each time step independently
        if self.transform is not None:
            C, T = voxel.shape[:2]
            aug_voxel = np.zeros_like(voxel)
            # Apply same spatial transform to all time steps
            aug_result = self.transform(image=voxel[0, 0], mask=mask)
            mask = aug_result["mask"]
            for c in range(C):
                for t in range(T):
                    aug_result = self.transform(
                        image=voxel[c, t], mask=np.zeros_like(mask)
                    )
                    aug_voxel[c, t] = aug_result["image"]
            voxel = aug_voxel

        # Convert to tensors
        # voxel: (2, T, H, W) → (T, 2, H, W) for SNN time-first format
        voxel_t = torch.from_numpy(voxel).permute(1, 0, 2, 3).float()  # (T, C, H, W)
        mask_t  = torch.from_numpy(mask).unsqueeze(0).float()            # (1, H, W)

        return {
            "events": voxel_t,                    # (T, C, H, W)
            "mask":   mask_t,                     # (1, H, W)
            "rgb_path": str(sample["rgb"]),
        }


# ──────────────────────────────────────────────────────────────────────
# DataLoader factory
# ──────────────────────────────────────────────────────────────────────

def build_dataloaders(cfg: dict):
    """
    Build train / val / test DataLoaders from config.

    Returns:
        train_loader, val_loader, test_loader
    """
    image_size = tuple(cfg["data"]["image_size"])
    T          = cfg["data"]["time_steps"]
    batch_size = cfg["training"]["batch_size"]
    num_workers = cfg["data"]["num_workers"]

    train_aug = get_train_augmentations(image_size)
    val_aug   = get_val_augmentations(image_size)

    train_ds = EventDataset(
        rgb_dir   = cfg["data"]["synthetic_rgb"],
        mask_dir  = cfg["data"]["synthetic_masks"],
        event_dir = cfg["data"]["synthetic_events"],
        image_size = image_size,
        time_steps = T,
        split = "train",
        transform = train_aug,
    )
    val_ds = EventDataset(
        rgb_dir   = cfg["data"]["synthetic_rgb"],
        mask_dir  = cfg["data"]["synthetic_masks"],
        event_dir = cfg["data"]["synthetic_events"],
        image_size = image_size,
        time_steps = T,
        split = "val",
        transform = val_aug,
    )
    test_ds = EventDataset(
        rgb_dir   = cfg["data"]["real_rgb"],
        mask_dir  = cfg["data"]["real_masks"],
        event_dir = cfg["data"]["real_events"],
        image_size = image_size,
        time_steps = T,
        split = "test",
        transform = val_aug,
    )

    # Split synthetic into train/val if no separate val set
    if len(val_ds) == 0 or len(val_ds) == len(train_ds):
        total = len(train_ds)
        n_val = int(total * cfg["data"]["val_split"])
        n_train = total - n_val
        train_ds, val_ds = random_split(
            train_ds, [n_train, n_val],
            generator=torch.Generator().manual_seed(42)
        )
        logger.info(f"Auto-split: {n_train} train / {n_val} val samples.")

    def make_loader(ds, shuffle: bool):
        return DataLoader(
            ds,
            batch_size=batch_size,
            shuffle=shuffle,
            num_workers=num_workers,
            pin_memory=cfg["data"].get("pin_memory", True),
            drop_last=shuffle,
        )

    return (
        make_loader(train_ds, shuffle=True),
        make_loader(val_ds,   shuffle=False),
        make_loader(test_ds,  shuffle=False),
    )